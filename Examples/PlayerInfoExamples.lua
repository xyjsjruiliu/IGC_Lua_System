-- ═══════════════════════════════════════════════════════════════
-- 命令列表
-- ═══════════════════════════════════════════════════════════════
--
-- 【查询命令】
--   /查询            - 查看自己的详细信息
--   /查询 玩家名     - 查看指定玩家的详细信息
--   /chaxun 玩家名   - 同上（拼音别名）
--   /query 玩家名    - 同上（英文别名）
--
-- 【在线列表命令】
--   /在线            - 查看在线玩家列表（第一页）
--   /在线 2          - 查看在线列表第二页
--   /在线 剑士       - 按职业筛选在线剑士
--   /在线 职业 魔法师 - 按职业筛选（带"职业"关键字）
--   /zaixian         - 同上（拼音别名）
--   /online          - 同上（英文别名）
--
